  <div style="margin-left: 10px;background-color:#181818;color: white;font-family: 'IBM Plex Serif', serif;"><br><br>
    <h2>How Criminology Works</h2>
    <p>In essence, criminologists examine every conceivable aspect of deviant behavior. That includes the impacts of crime on individual victims and their families, society at large, and even criminals themselves.</p>
    <p>Some of the specific areas that criminology covers include:</p>
    <li>Frequency of crimes </li>
    <li>Location of crimes</li>
    <li>  Causes of crimes</li>
    <li>   Types of crimes</li>
    <li>   Social and individual consequences of crimes</li>
    <li>   Social reactions to crime</li>
    <li>    Individual reactions to crime</li>
    <li>   Governmental reactions to crime</li><br>
    <p>Criminologists conduct research and analyze data to help understand, deter, and prevent crime. They also develop theories based on the research they conduct to help translate data into action. Some criminologists also evaluate, develop, and implement criminal justice policies and procedures.</p>

